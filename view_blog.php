                        <h2>متلازمة داون عند الأطفال أعراض و أسباب</h2>
                        <p>تُعتبر متلازمة داون عند الأطفال واحدة من أبرز الحالات الوراثية التي تؤثر عليهم. تحدث هذذ المتلازمة نتيجة بعض العوامل، حيث تؤثر على النمو الجسدي والعقلي للأطفال، مما يجعلها موضوعًا مهمًا للبحث والدراسة. كما تتنوع أعراضها، حيث تشمل تأخر في النمو، وملامح وجهية مميزة، ومشكلات صحية مرتبطة. فهذا الأمر يستدعي دعمًا خاصًا من الأسرة والمجتمع.</p>

                        <h2>أنواع متلازمة داون</h2>

                        <p>تتجلى التغيرات الكروموسومية التي تسبب متلازمة داون في ثلاث صور مختلفة، فهذا يؤدي إلى تنوع الأشكال التي قد تظهر بها هذه المتلازمة، حيث تختلف الأعراض ودرجات التأثير على الأشخاص وفقًا لنوعها، وفيما يلي الأنواع الرئيسية لها:</p>

                        <ul>
                            <li><strong>التثلث الصبغي 21:</strong> يحدث نتيجة عدم انفصال زوج الكروموسومات رقم 21 في البويضة أو الحيوان المنوي، ورغم عدم معرفة السبب بدقة، إلا أنه يشكل نسبة تتراوح بين 90-95% من حالات متلازمة داون، يُعتبر هذا النوع هو الأكثر شيوعًا، وخاصة النساء اللاتي تقدمن في السن ويكونانا حوامل.</li>
                            <li><strong>الفسيفسائية:</strong> تحتوي بعض الخلايا على ثلاث نسخ من الكروموسوم 21 بينما تكون الخلايا الأخرى طبيعية، أي أن مجموعة من الخلايا قد تكون محتوية على 46 كروموسومًا، بينما تحتوي خلايا أخرى على 47 كروموسومًا نتيجة التثلث.</li>
                            <li><strong>الانتقال الكروموسومي:</strong> في هذه الحالة، يحدث نقل جزء من الكروموسوم 21 لينضم إلى أحد الكروموسومات الأخرى، عادة ما يكون الاندماج مع الكروموسوم 14 أو 15، بنتيجة لذلك، يبقى عدد الكروموسومات في الخلية 46، ولكن جزء من الكروموسوم 21 يُدمج مع كروموسوم آخر، فيُمثل هذا النوع حوالي 3-4% من حالات متلازمة داون، وهو النوع الوحيد الذي لا يعتمد على عمر الأبوين ويمكن أن ينتقل وراثيًا بغض النظر عن أعمارهما.</li>
                        </ul>

