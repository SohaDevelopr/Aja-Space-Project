                        </ul>

                        <h2>مضاعفات الإصابة بمتلازمة داون عند الأطفال</h2>

                        <p>قد يواجه بعض المصابين بمتلازمة داون عند الأطفال مجموعة متنوعة من المضاعفات، والتي تظهر أكثر مع التقدم في العمر، ومن أبرزها:</p>

                        <ul>
                            <li><strong>عيوب القلب:</strong> نصف الأطفال المصابين يولدون مع مشاكل في القلب، والتي قد تهدد حياتهم وتستلزم إجراء عمليات جراحية في مرحلة المبكرة.</li>
                            <li><strong>عيوب الجهاز الهضمي:</strong> يمكن أن تظهر تشوهات في الجهاز الهضمي لدى بعض الأطفال المصابين، والتي تشمل مشكلات في الأمعاء والمريء والقصبة الهوائية والشرج، كما يزداد احتمال تعرضهم لمشكلات هضمية مثل انسداد الجهاز الهضمي أو حرقة المعدة أو الداء البطني.</li>
                            <li><strong>اضطرابات المناعة:</strong> بسبب الفروقات في الجهاز المناعي. تكون نسبة الإصابة بأمراض المناعة الذاتية وأنواع معينة من السرطان مرتفعة، بالإضافة إلى الأمراض المعدية مثل الالتهاب الرئوي.</li>
                            <li><strong>انقطاع النفس أثناء النوم:</strong> قد تؤدي التغيرات في الأنسجة الرخوة والأمور الهيكلية إلى مشاكل في مجرى الهواء، مما يجعل الأطفال والبالغين المصابين بمتلازمة داون عرضة لانقطاع النفس الانسدادي أثناء النوم.</li>
                            <li><strong>السمنة:</strong> يميل الأشخاص المصابون إلى زيادة الوزن بشكل أكبر مقارنة بالعامة، مما قد يؤثر على صحتهم العامة.</li>
                            <li><strong>مشكلات الحبل الشوكي:</strong> يعاني بعض الأشخاص من عدم استقرار في الفقرتين العلويتين في الرقبة، مما يزيد من خطر إصابة الحبل الشوكي نتيجة لتحركات غير طبيعية.</li>
                            <li><strong>ابيضاض الدم:</strong> المصابون بهذا المرض أكثر عرضة للإصابة بسرطان الدم.</li>
                            <li><strong>الخرف:</strong> يواجه المصابون بالمتلازمة مخاطر أعلى للإصابة بالخرف، حيث قد تبدأ أعراضه مبكرًا في عمر الخمسين. كما أن لديهم احتمالية أكبر للإصابة بمرض الزهايمر.</li>
                            <li><strong>مشاكل صحية أخرى:</strong> يمكن أن تترافق متلازمة داون مع حالات صحية متعددة أخرى، مثل مشكلات الغدد الصماء، والأسنان، والنوبات، والالتهابات، ومشاكل السمع والبصر.</li>
                        </ul>

                        <h2>ما هو تشخيص متلازمة داون</h2>

                        <p>يوصي الأطباء بإجراء مجموعة من الفحوصات خلال فترة الحمل للتحقق من احتمال إصابة الجنين بمتلازمة داون. تتوفر عدة اختبارات تصويرية قادرة على الكشف عن المتلازمة، ومع ذلك، فإن هذه الفحوصات قد تعطي أحيانًا نتائج إيجابية خاطئة، مما يؤدي إلى ظهور نتائج تشير إلى احتمال وجود المتلازمة بينما الجنين غير مصاب بها.</p>

